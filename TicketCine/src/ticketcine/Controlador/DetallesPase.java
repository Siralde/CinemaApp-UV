public class DetallesPase {

    public Object hora;
    public Object sala;
    public Object pelicula;
    public Object cine;
    public Date fecha;
    public float precio;
    
    public DetallesPase(Object hora, Object sala, Object cine, Object pelicula, Date fecha, float precio)
    {
        this.hora = hora;
        this.sala = sala;
        this.cine = cine;
        this.pelicula = pelicula;
        this.fecha = fecha;
        this.precio = precio;
    }
}
